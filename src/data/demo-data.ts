// Helper to generate dates relative to today so demo data is always recent
function daysAgo(days: number, time?: string): Date {
  const d = new Date()
  d.setDate(d.getDate() - days)
  if (time) {
    const [h, m] = time.split(':').map(Number)
    d.setHours(h, m, 0, 0)
  } else {
    d.setHours(0, 0, 0, 0)
  }
  return d
}

